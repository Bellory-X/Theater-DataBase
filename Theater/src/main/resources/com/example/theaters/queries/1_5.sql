SELECT COUNT(*)
FROM Служащие, Музыканты
WHERE Служащие.Театр = '#'
  AND Служащие.Является_Работником = 'True'
  AND Музыканты.Id_Служащего = Служащие.Id
  AND Служащие.Опыт >= #
  AND Служащие.Опыт <= #
  AND Служащие.Пол = '#'
  AND EXTRACT(YEAR FROM Служащие.Дата_рождения) = #
  AND Служащие.Колличество_Детей >= #
  AND Служащие.Колличество_Детей <= #
  AND Служащие.Зарплата >= #
  AND Служащие.Зарплата <= #